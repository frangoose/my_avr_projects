void NOKIA_Send_Command(char cmd)
{
	CLR_NOKIA_CE;
	CLR_NOKIA_DC;
	SPI_MasterTransmit(cmd);
	SET_NOKIA_CE;
}

void NOKIA_Send_Data(char data)
{
	CLR_NOKIA_CE;
	SET_NOKIA_DC;
	SPI_MasterTransmit(data);
	SET_NOKIA_CE;
}

void NOKIA_Init(void)
{
	DDRD = (1<<DDD5) | (1<<DDD7); // Set pins as outputs for DC and CE
	SET_NOKIA_CE; // Set CE pin to begins command transmission protocol
	NOKIA_Send_Command(0x21); // function set PD=0 (non-powerdown), V=0(hori addr mode), H=1 (extd instr set mode)
	NOKIA_Send_Command(0xB0); // set Vop (contrast)
	NOKIA_Send_Command(0x04); // set Temp Coefficient
	NOKIA_Send_Command(0x14); // set LCD Bias mode 1:48
	NOKIA_Send_Command(0x20); // function set PD=0 (non-powerdown), V=0(hori addr mode), H=0 (basic instr set mode)
	NOKIA_Send_Command(0x0C); // display control set normal mode D=1, E=0
	NOKIA_Fill_Display(CLR_PIXEL);
	NOKIA_Send_Command(0x40); // init Y-addr to 0
	NOKIA_Send_Command(0x80); // init x-addr to 0
}

void NOKIA_Invert(void)
{
	NOKIA_Send_Command(0x0D);
}

void NOKIA_Reset_Origin(uint8_t x_position, uint8_t y_position){
	NOKIA_Send_Command(0x40); // init Y-addr to 0
	NOKIA_Send_Command(0x80); // init x-addr to 0
}

void NOKIA_Draw_Chkr(void)
{
	for(uint16_t i = 0; i < (LCD_WIDTH * LCD_HEIGHT / 8); i+=2)
	{
		display_map[i] = (0xAA);
		display_map[i+1] = (0x55);
	}
}

void NOKIA_Fill_Display(char fill)
{
	char fill_type;
	if(fill == 1)
	{
		fill_type = 0xFF;
	}
	else
	{
		fill_type = 0x00;
	}
	for(uint16_t i = 0; i < (LCD_WIDTH * LCD_HEIGHT / 8); i++) //504 addresses to write 1 byte to
	{
		display_map[i] = fill_type;
	}
}

void NOKIA_Update_Display()
{
	NOKIA_Reset_Origin(0, 0);
	for(uint16_t i = 0; i < LCD_WIDTH * LCD_HEIGHT / 8; i++)
	{
		NOKIA_Send_Data(display_map[i]);
	}
}

void NOKIA_Draw_Char(char ascii_char, uint8_t x_position, uint8_t y_position)
{
	char column;
	for(int8_t i = 0; i < 5; i++)
	{
		column = ASCII[ascii_char - 0x20][i];
		for(int8_t j = 0; j < 8; j++)
		{
			if(column & (0x01 << j))
			{
				NOKIA_Set_Pixel(x_position + i, y_position + j, SET_PIXEL);
			}
			else
			{
				NOKIA_Set_Pixel(x_position + i, y_position + j, CLR_PIXEL);
			}
		}
	}
}

void NOKIA_Draw_String(char ascii_chars[], uint8_t size, uint8_t x_position, uint8_t y_position)
{
	uint16_t i = 0;
	while(i < size - 1){
		NOKIA_Draw_Char(ascii_chars[i], x_position, y_position);
		x_position +=6;
		i++;
	}
}

void NOKIA_Set_Pixel(uint8_t x_position, uint8_t y_position, uint8_t bw)
{
	uint8_t y_shift = y_position % 8;
	if(x_position < LCD_WIDTH)
	{
		if(y_position < LCD_HEIGHT)
		{
			if(bw)
			{
				display_map[x_position + (y_position/8) * LCD_WIDTH] |= (1 << y_shift);
			}
			else
			{
				display_map[x_position + (y_position/8) * LCD_WIDTH] &= ~(1 << y_shift);
			}
		}
	}
}

void NOKIA_Draw_Line(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, uint8_t bw)
{
	int16_t dy = y1 - y0;
	int16_t dx = x1 - x0;
	int8_t stepx, stepy;
	
	if(dy < 0)
	{
		dy = -dy;
		stepy = -1;
	}
	else
	{
		stepy = 1;
	}
	if(dx < 0)
	{
		dx = -dx;
		stepx = -1;
	}
	else
	{
		stepx = 1;
	}
	dy <<= 1;
	dx <<= 1;
	NOKIA_Set_Pixel(x0, y0, bw);
	if(dx > dy)
	{
		int16_t frac = dy - (dx >> 1);
		while(x0 != x1)
		{
			if(frac >= 0)
			{
				y0 += stepy;
				frac -= dx;
			}
			x0 += stepx;
			frac += dy;
			NOKIA_Set_Pixel(x0, y0, bw);
		}
	}
	else
	{
		int16_t frac = dx - (dy >> 1);
		while(y0 != y1)
		{
			if(frac >= 0)
			{
				x0 += stepx;
				frac -= dy;
			}
			y0 += stepy;
			frac += dx;
			NOKIA_Set_Pixel(x0, y0, bw);
		}
	}
}

void NOKIA_Draw_Rect(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, uint8_t bw)
{
	NOKIA_Draw_Line(x0,y0,x1,y0,bw);
	NOKIA_Draw_Line(x0,y1,x1,y1,bw);
	NOKIA_Draw_Line(x1,y0,x1,y1,bw);
	NOKIA_Draw_Line(x0,y0,x0,y1,bw);
}